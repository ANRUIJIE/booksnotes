const CN = ['零','一','二','三','四','五','六','七','八','九'];
function chNum(n) {
  if (n <= 10) return `第${['','一','二','三','四','五','六','七','八','九','十'][n]}章`;
  if (n < 20) return `第十${CN[n - 10]}章`;
  if (n === 20) return '第二十章';
  if (n < 30) return `第二十${CN[n - 20]}章`;
  if (n === 30) return '第三十章';
  if (n < 40) return `第三十${CN[n - 30]}章`;
  if (n === 40) return '第四十章';
  if (n < 50) return `第四十${CN[n - 40]}章`;
  if (n === 50) return '第五十章';
  return `第五十${CN[n - 50]}章`;
}

function ch(id, n, title, file) {
  return { id: `v3c${String(id).padStart(2, '0')}`, num: chNum(n), title, file };
}

